 static int
tls2_xcheck(real *x)
{
	real *x1 = tls2_old_x, *xe = x + 37;
	errno = 0;
	if (tls2_xkind >= 0) {
		while(*x++ == *x1++)
			if (x == xe)
				return 0;
		--x, --x1;
		}
	do *x1++ = *x++;
		while(x < xe);
	tls2_xkind = 0;
	return 1;
	}
