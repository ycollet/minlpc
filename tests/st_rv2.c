 static int
st_rv2_xcheck(real *x)
{
	real *x1 = st_rv2_old_x, *xe = x + 20;
	errno = 0;
	if (st_rv2_xkind >= 0) {
		while(*x++ == *x1++)
			if (x == xe)
				return 0;
		--x, --x1;
		}
	do *x1++ = *x++;
		while(x < xe);
	st_rv2_xkind = 0;
	return 1;
	}
